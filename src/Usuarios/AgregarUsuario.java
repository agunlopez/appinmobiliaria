    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if(txtUsuario.getText().length()>=3){
            
        boolean existe=false;
        try {
            
            ArrayList<Usuario> listaUsuarios=GestorUsuario.traerTodosLosUsuarios();
            for(int i=0;i<listaUsuarios.size();i++){
                if(listaUsuarios.get(i).getUsuario().equals(txtUsuario.getText())){
                    existe=true;
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(AgregarUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(existe==false){      
        if(txtClave.getText().length()>3 && txtClave.getText().length()<13){
            if(txtClave.getText().equals(txtClaveRepetida.getText())){
            Usuario usuario=new Usuario();
            usuario.setUsuario(txtUsuario.getText());
            usuario.setClave(txtClave.getText());
            usuario.setTipo("EMPLEADO");

            Eventos evento=new Eventos();            
            evento.setUsuario(InicioSesion.usuarioEnSesion);
            evento.setTipo("USUARIO");
            evento.setDescipcion("Se agrego el usuario "+usuario.getUsuario());
            evento.setFecha(GestorEventos.getFechaActual());
            
            int r=GestorUsuario.AgregarUsuario(usuario);
            if(r==1){
                JOptionPane.showMessageDialog(new JDialog(),"Agregado Correctamente");
                try {
                    int e=GestorEventos.AltaEvento(evento);
                } catch (IOException ex) {
                    Logger.getLogger(AgregarUsuario.class.getName()).log(Level.SEVERE, null, ex);
                }
                this.dispose();
            }
        }else{
            JOptionPane.showMessageDialog(new JDialog(),"Las Contrase単as son Diferentes");
        }
        }else{
            JOptionPane.showMessageDialog(new JDialog(),"La Contrase単a debe tener entre 4 y 12 caracteres");
        }
        
        }else{
            JOptionPane.showMessageDialog(new JDialog(),"El Usuario ya existe");
        }
        
        }else{
            JOptionPane.showMessageDialog(new JDialog(),"Debe ingresar al menos 3 caracteres");            
        }
        

        // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

