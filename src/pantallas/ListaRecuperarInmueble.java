    private void tablaRecuperarInmueblesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaRecuperarInmueblesMouseClicked
        if(evt.getClickCount()==2){
            String direccion=tablaRecuperarInmuebles.getValueAt(tablaRecuperarInmuebles.getSelectedRow(), 1).toString();
            int idInmueble=Integer.parseInt(tablaRecuperarInmuebles.getValueAt(tablaRecuperarInmuebles.getSelectedRow(), 0).toString());
            Object[] opciones = {"Aceptar", "Cancelar"};
            int eleccion = JOptionPane.showOptionDialog(null, "Â¿Desea recuperar el Inmueble "+direccion+" ?", "Mensaje de Confirmacion",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.WARNING_MESSAGE, null, opciones, "Cancelar");
            if (eleccion == JOptionPane.YES_OPTION) {       
                int r=GestorModificarInmueble.ModificarEstadoDisponible(idInmueble);
                Eventos evento=new Eventos();            
           
                evento.setUsuario(InicioSesion.usuarioEnSesion);            
                evento.setTipo("INMUEBLE");          
                evento.setDescipcion("Se recupero el Inmueble "+direccion);         
                evento.setFecha(GestorEventos.getFechaActual());
                if(r==1){
                    JOptionPane.showMessageDialog(new JDialog(),"El Inmueble se recupero con exito");
                    try {
                        int e=GestorEventos.AltaEvento(evento);
                    } catch (IOException ex) {
                        Logger.getLogger(ListaRecuperarInmueble.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }
       
    }//GEN-LAST:event_tablaRecuperarInmueblesMouseClicked

